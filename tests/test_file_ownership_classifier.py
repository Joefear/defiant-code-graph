from pathlib import Path

from src.file_ownership_classifier import build_file_ownership_facts


def test_build_file_ownership_facts(tmp_path: Path) -> None:
    generated_file = tmp_path / "a.generated.py"
    locked_file = tmp_path / "b.py"
    manual_file = tmp_path / "c.py"
    unknown_file = tmp_path / "d.bin"

    generated_file.write_text("# @generated\n", encoding="utf-8")
    locked_file.write_text("# @locked\n", encoding="utf-8")
    manual_file.write_text("def foo():\n    pass\n", encoding="utf-8")
    unknown_file.write_bytes(b"\xff")

    ownership_facts = build_file_ownership_facts(tmp_path)

    assert ownership_facts == [
        {"file_path": "a.generated.py", "ownership": "generated"},
        {"file_path": "b.py", "ownership": "locked"},
        {"file_path": "c.py", "ownership": "manual"},
        {"file_path": "d.bin", "ownership": "unknown"},
    ]


def test_governed_classification(tmp_path: Path) -> None:
    (tmp_path / "a.py").write_text("# @governed\n", encoding="utf-8")
    (tmp_path / "b.py").write_text("# ownership: governed\n", encoding="utf-8")

    facts = {f["file_path"]: f["ownership"] for f in build_file_ownership_facts(tmp_path)}
    assert facts["a.py"] == "governed"
    assert facts["b.py"] == "governed"


