def test_content_based_generated_classification(tmp_path: Path) -> None:
    markers = [
        ("a.py", "# auto-generated\n"),
        ("b.py", "# autogenerated\n"),
        ("c.py", "# generated by foo\n"),
        ("d.py", "# this file was generated\n"),
        ("e.py", "# do not edit\n"),
    ]
    for name, content in markers:
        (tmp_path / name).write_text(content, encoding="utf-8")

    facts = {f["file_path"]: f["ownership"] for f in build_file_ownership_facts(tmp_path)}
    for name, _ in markers:
        assert facts[name] == "generated", f"{name} should be generated"


def test_ownership_markers_take_priority_over_generated_content(tmp_path: Path) -> None:
    # A file with both an ownership marker and a generated marker yields the ownership category.
    (tmp_path / "a.py").write_text("# @locked\n# auto-generated\n", encoding="utf-8")

    facts = {f["file_path"]: f["ownership"] for f in build_file_ownership_facts(tmp_path)}
    assert facts["a.py"] == "locked"


def test_filename_generated_takes_priority_over_content_markers(tmp_path: Path) -> None:
    # A .generated. filename is classified without reading content.
    (tmp_path / "a.generated.py").write_text("# @locked\n", encoding="utf-8")

    facts = {f["file_path"]: f["ownership"] for f in build_file_ownership_facts(tmp_path)}
    assert facts["a.generated.py"] == "generated"
